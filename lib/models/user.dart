
// Class to hold user information
class User {
  String FirstName;
  String LastName;
  String password;
  String email;
  String phone;
  String age;
  String Gender;
  String Nationality;


  User({
    required this.FirstName,
    required this.LastName,
    required this.password,
    required this.email,
    required this.phone,
    required this.age,
    required this.Gender,
    required this.Nationality,

  });
}
